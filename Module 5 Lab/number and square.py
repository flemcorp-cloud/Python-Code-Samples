# A program that will loop to print each number and its square on a new line.

# List of numbers
numbers = [12, 10, 32, 3, 66, 17, 42, 99, 20]

# Loop through each number in the list and
# format the output to display both the number and its square on the same line.
for number in numbers:
    print(f"{number} {number ** 2}")


